'''
Faca um programa em Python que recebe dois numeros
e mostre sua soma.
'''

# Recebe os numeros do teclado:
n1 = input ("Informe o primeiro numero: ")
n2 = input ("Informe o segundo numero: ")

# Converte para numerico:
n1 = float (n1)
n2 = float (n2)

# Escreve soma na tela:
soma = n1 + n2
print (soma)